package exercises.lessonsAutomationTasks.lecture6.task2.den;

public class CenterCoordinates {
    private double x;
    private double y;

    public CenterCoordinates(double x, double y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "x = " + x + ", " + "y = " + y;
    }
}
